package model;

public class Message {
	private String nickName;
	private String level;
	
	
	
	public Message(String nickName, String level) {

		this.nickName = nickName;
		this.level = level;
	}



	public String getNickName() {
		return nickName;
	}



	public String getLevel() {
		return level;
	}
	
	
	
	

}
